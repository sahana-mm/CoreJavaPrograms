package destinationProgs;

public class PreIncrementatationAndPostIncrementation {

	public static void main(String[] args) {
		byte b=5;
		b++;
		System.out.println(b);
		System.out.println();
		byte b1=5;
		++b1;
		System.out.println(b1);
		System.out.println();
		byte b2=5;
		int c=b2++;
		System.out.println(c);

	}

}
